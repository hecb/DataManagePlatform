package com.huaxia.base.bean;

import java.util.List;

/**
 * @description 分页参数 
 * @author : zhangy
 * @date : 2016年12月29日 上午9:53:17
 */
public class DataTablePageHandler{
    private int iDisplayStart; //起始记录，0表示第一条
    private int iDisplayLength;//每页显示的记录数
    private long iTotalRecords;//实际的行数
    private long iTotalDisplayRecords;////显示的行数,这个要和上面写的一样
    private int sEcho; //请求服务器端次数
    private List<?> aaData;//数据
    
    
	public int getiDisplayStart() {
		return iDisplayStart;
	}
	public void setiDisplayStart(int iDisplayStart) {
		this.iDisplayStart = iDisplayStart;
	}
	public int getiDisplayLength() {
		return iDisplayLength;
	}
	public void setiDisplayLength(int iDisplayLength) {
		this.iDisplayLength = iDisplayLength;
	}
	public long getiTotalDisplayRecords() {
		return iTotalDisplayRecords;
	}
	public void setiTotalDisplayRecords(long iTotalDisplayRecords) {
		this.iTotalDisplayRecords = iTotalDisplayRecords;
	}
	public long getiTotalRecords() {
		return iTotalRecords;
	}
	public void setiTotalRecords(long iTotalRecords) {
		this.iTotalRecords = iTotalRecords;
	}
	public int getsEcho() {
		return sEcho;
	}
	public void setsEcho(int sEcho) {
		this.sEcho = sEcho;
	}
	public List<?> getAaData() {
		return aaData;
	}
	public void setAaData(List<?> aaData) {
		this.aaData = aaData;
	}
    

    
	
    
    
}
